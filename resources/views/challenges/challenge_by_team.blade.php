                    Atticus, Jack, Cora, and Maia are 11th grade students at Divine High School. They have been working in a field science class with their teacher, Ms. Hayes, and the researchers at MPG Ranch and Raptor View Research Institute(RVRI) for the past year. They have spent a lot of time in the field working side by side with the researchers studying golden eagles.

                    The RVRI researchers Rob and Adam are very concerned about the golden eagle population and the risks the migrating birds face all along the flyway. They have explained to the students that birds and humans don't view the landscape, borders, and boundaries in the same way. Laws or policies in different jurisdictions have a huge impact on management of wildlife. In their view, there is an urgent need for awareness, problem solving, and solution finding before these beautiful animals are gone.

                    Ms. Hayes started this quarter's class with the announcement about the International Student Symposium--Birds Without Borders--sponsored by IABB. After the four of them talked it over and asked their parents, they agreed that they would form a student team and go to the Symposium. They are excited (and a little bit nervous) to speak in front of big crowds of important people but they are up for the challenge.

                    They began working on their presentation but lacked direction and focus. They talked to Rob and Adam who agreed to help them think through the project and come up with a graphical abstract that explained the work they were doing and proposed an eagle stewardship solution idea. Because the team was so enthusiastic about their presentation, Rob and Adam encouraged them to invite other students along the flyway to participate too.
                    </p>

                    <h3>Your Challenge:</h3>
                    <p>
                            You and your team are challenged to research the information and data  provided by MPG Ranch and RVRI to develop a graphical abstract on Eagle Stewardship.  Your graphical abstract must include information about: 
                            
                            specifics about the flyway especially as it pertains to your location, 
                            a map, 
                            data,  
                            identification of the biggest risk to migrating birds in your area, 
                             your proposed solution to those risks and recommendations for policy change to increase eagle stewardship. 
                            </p>

                    <h4>Create a Graphical Abstract to Submit</h4>

                    <p>After analyzing the data and researching, create a visual graphical abstract similar to those in scientific papers to interpret the data and support your recommendation for eagle stewardship in your area. The graphical abstract should contain the following:

                            1. The biggest risk to migrating birds in your area as backed by research..
                            2. Pertinent maps used to research.
                            3. Graph depiction of the data used.
                            4. Concise summary of the critical findings of your research.
                            5. Concise description of your proposed recommendation including potential policy recommendations.
                            6. Art work to illustrate the concepts.
                            
                            Your graphical abstract should be persuasive in nature and be backed by factual evidence. It should appropriate to present to policy makers in your area, region, nationally, and internationally.
                            
                            Note: The graphical abstract should be a one page pdf, one powerpoint slide or jpeg for easy uploading.
                            
                            </p>

